const patterns = INPUT.split('\n\n')

const sum = patterns
  .map((pattern, patternIndex) => {
    const rows = pattern.split('\n')
    const columns = [...Array(rows[0].length).keys()].map(colNumber => {
      return [...Array(rows.length).keys()]
        .map(rowNumber => {
          return (
            rows[rowNumber][colNumber] +
            (rowNumber === pattern.length - 1 ? ' ' : '')
          )
        })
        .join('')
    })

    let rowtotal = 0
    let columnTotal = 0

    let isValidMirror = false
    let endIndex = 0
    let startIndex = 0

    // horizontal row
    for (let ind = 0; ind < rows.length; ind++) {
      if (rows[ind] === rows[ind + 1]) {
        isValidMirror = true
        endIndex = ind + 1
        startIndex = ind
        if (startIndex >= 0 && endIndex < rows.length) {
          while (endIndex < rows.length && startIndex >= 0) {
            if (rows[startIndex] !== rows[endIndex]) {
              isValidMirror = false
              break
            }
            endIndex++
            startIndex--
          }
        }
      }
      if (isValidMirror) {
        rowtotal += (ind + 1) * 100
        break
      }
    }

    endIndex = 0
    startIndex = 0
    isValidMirror = false
    // vertical column
    for (let ind = 0; ind < columns.length; ind++) {
      if (columns[ind] === columns[ind + 1]) {
        isValidMirror = true
        endIndex = ind + 1
        startIndex = ind
        while (endIndex < columns.length && startIndex >= 0) {
          if (columns[startIndex] !== columns[endIndex]) {
            isValidMirror = false
            break
          }
          endIndex++
          startIndex--
        }
      }

      if (isValidMirror) {
        columnTotal += ind + 1
        break
      }
    }

    return columnTotal + rowtotal
  })
  .reduce((a, b) => a + b, 0)

console.log('sum: ', sum)

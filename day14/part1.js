const indexOfAll = (arr, val) =>
  arr.reduce((acc, el, i) => (el === val ? [...acc, i] : acc), [])

const closest = (arr, goal) =>
  arr && arr.length ? arr.findLastIndex(a => a < goal) : 0

const transpose = arr => {
  return [...Array(arr[0].length).keys()].map(stringIndex => {
    return arr.map(r => r[stringIndex])
  })
}

const untranspose = arr => {
  return [...Array(arr[0].length).keys()].map(stringIndex => {
    return arr.map(r => r[stringIndex])
  })
}

